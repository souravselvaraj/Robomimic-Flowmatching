"""
Implementation of Conditional Flow Matching (rectified flow style) for policy learning.

Trains a velocity field v_theta(x_t, t, obs) with the conditional flow matching objective
(Lipman et al., 2023; Liu et al., 2022) over the linear interpolation path between
Gaussian noise x_0 and ground-truth action sequences x_1:

    x_t = (1 - (1 - sigma_min) * t) * x_0 + t * x_1
    target velocity u_t = x_1 - (1 - sigma_min) * x_0

At inference time, actions are generated by integrating the learned ODE from t=0 to t=1
with a fixed number of Euler (or midpoint) steps, conditioned on encoded observations.

Reuses the ConditionalUnet1D backbone and observation encoding from Diffusion Policy.
"""
from collections import OrderedDict, deque
import torch
import torch.nn as nn
import torch.nn.functional as F
from diffusers.training_utils import EMAModel

import robomimic.models.obs_nets as ObsNets
import robomimic.models.diffusion_policy_nets as DPNets
import robomimic.utils.tensor_utils as TensorUtils
import robomimic.utils.torch_utils as TorchUtils
import robomimic.utils.obs_utils as ObsUtils

from robomimic.algo import register_algo_factory_func, PolicyAlgo
from robomimic.algo.diffusion_policy import replace_bn_with_gn

from robomimic_cfm.transformer_nets import ConditionalTransformer1D


@register_algo_factory_func("flow_matching")
def algo_config_to_class(algo_config):
    """
    Maps algo config to the FlowMatching algo class to instantiate, along with additional algo kwargs.

    Args:
        algo_config (Config instance): algo config

    Returns:
        algo_class: subclass of Algo
        algo_kwargs (dict): dictionary of additional kwargs to pass to algorithm
    """
    # a single algo class handles both backbones; the velocity network is chosen
    # in _create_networks based on which backbone is enabled
    if algo_config.unet.enabled or algo_config.transformer.enabled:
        return FlowMatchingUNet, {}
    else:
        raise RuntimeError("flow_matching: enable exactly one of algo.unet or algo.transformer")


class FlowMatchingUNet(PolicyAlgo):
    def _create_networks(self):
        """
        Creates networks and places them into @self.nets.
        """
        # set up different observation groups for @MIMO_MLP
        observation_group_shapes = OrderedDict()
        observation_group_shapes["obs"] = OrderedDict(self.obs_shapes)
        encoder_kwargs = ObsUtils.obs_encoder_kwargs_from_config(self.obs_config.encoder)

        obs_encoder = ObsNets.ObservationGroupEncoder(
            observation_group_shapes=observation_group_shapes,
            encoder_kwargs=encoder_kwargs,
        )
        # IMPORTANT!
        # replace all BatchNorm with GroupNorm to work with EMA
        # performance will tank if you forget to do this!
        obs_encoder = replace_bn_with_gn(obs_encoder)

        obs_dim = obs_encoder.output_shape()[0]

        # velocity field network - the output is interpreted as the flow velocity
        # (instead of a denoising target). Two interchangeable backbones share the
        # same call signature: net(sample, timestep, global_cond) -> velocity.
        global_cond_dim = obs_dim * self.algo_config.horizon.observation_horizon
        if self.algo_config.transformer.enabled:
            # 1D DiT over the predicted action chunk (AdaLN conditioning on t + obs)
            velocity_net = ConditionalTransformer1D(
                input_dim=self.ac_dim,
                global_cond_dim=global_cond_dim,
                n_emb=self.algo_config.transformer.n_emb,
                n_layer=self.algo_config.transformer.n_layer,
                n_head=self.algo_config.transformer.n_head,
                p_drop=self.algo_config.transformer.p_drop,
                max_positions=self.algo_config.horizon.prediction_horizon,
                diffusion_step_embed_dim=self.algo_config.transformer.diffusion_step_embed_dim,
                causal=self.algo_config.transformer.causal,
                mlp_ratio=self.algo_config.transformer.mlp_ratio,
                cross_attn=self.algo_config.transformer.cross_attn,
                n_obs_steps=self.algo_config.horizon.observation_horizon,
            )
        else:
            # same convolutional backbone as Diffusion Policy
            velocity_net = DPNets.ConditionalUnet1D(
                input_dim=self.ac_dim,
                global_cond_dim=global_cond_dim,
                diffusion_step_embed_dim=self.algo_config.unet.diffusion_step_embed_dim,
                down_dims=self.algo_config.unet.down_dims,
                kernel_size=self.algo_config.unet.kernel_size,
                n_groups=self.algo_config.unet.n_groups,
            )

        # the final arch has 2 parts
        nets = nn.ModuleDict({
            "policy": nn.ModuleDict({
                "obs_encoder": obs_encoder,
                "velocity_net": velocity_net
            })
        })

        nets = nets.float().to(self.device)

        # setup EMA
        ema = None
        if self.algo_config.ema.enabled:
            ema = EMAModel(model=nets, power=self.algo_config.ema.power)

        # set attrs
        self.nets = nets
        self.ema = ema
        self.action_check_done = False
        self.obs_queue = None
        self.action_queue = None

    def _timestep_embedding_scale(self):
        """
        Flow matching time lives in [0,1] but the sinusoidal embedding in the UNet
        expects diffusion-style integer-scale timesteps, so scale t before embedding.
        """
        return self.algo_config.fm.time_embed_scale

    def process_batch_for_training(self, batch):
        """
        Processes input batch from a data loader to filter out
        relevant information and prepare the batch for training.

        Args:
            batch (dict): dictionary with torch.Tensors sampled
                from a data loader

        Returns:
            input_batch (dict): processed and filtered batch that
                will be used for training
        """
        To = self.algo_config.horizon.observation_horizon
        Tp = self.algo_config.horizon.prediction_horizon

        input_batch = dict()
        input_batch["obs"] = {k: batch["obs"][k][:, :To, :] for k in batch["obs"]}
        input_batch["goal_obs"] = batch.get("goal_obs", None) # goals may not be present
        input_batch["actions"] = batch["actions"][:, :Tp, :]

        # check if actions are normalized to [-1,1]
        if not self.action_check_done:
            actions = input_batch["actions"]
            in_range = (-1 <= actions) & (actions <= 1)
            all_in_range = torch.all(in_range).item()
            if not all_in_range:
                raise ValueError("'actions' must be in range [-1,1] for Flow Matching! Check if hdf5_normalize_action is enabled.")
            self.action_check_done = True

        return TensorUtils.to_device(TensorUtils.to_float(input_batch), self.device)

    def train_on_batch(self, batch, epoch, validate=False):
        """
        Training on a single batch of data.

        Args:
            batch (dict): dictionary with torch.Tensors sampled
                from a data loader and filtered by @process_batch_for_training

            epoch (int): epoch number - required by some Algos that need
                to perform staged training and early stopping

            validate (bool): if True, don't perform any learning updates.

        Returns:
            info (dict): dictionary of relevant inputs, outputs, and losses
                that might be relevant for logging
        """
        B = batch["actions"].shape[0]
        sigma_min = self.algo_config.fm.sigma_min

        with TorchUtils.maybe_no_grad(no_grad=validate):
            info = super(FlowMatchingUNet, self).train_on_batch(batch, epoch, validate=validate)
            actions = batch["actions"]

            # encode obs
            inputs = {
                "obs": batch["obs"],
                "goal": batch["goal_obs"]
            }
            for k in self.obs_shapes:
                # first two dimensions should be [B, T] for inputs
                assert inputs["obs"][k].ndim - 2 == len(self.obs_shapes[k])

            obs_features = TensorUtils.time_distributed(inputs, self.nets["policy"]["obs_encoder"], inputs_as_kwargs=True)
            assert obs_features.ndim == 3  # [B, T, D]

            obs_cond = obs_features.flatten(start_dim=1)

            # sample noise (source distribution) and per-sample flow time t ~ U(0,1)
            noise = torch.randn(actions.shape, device=self.device)
            t = torch.rand((B,), device=self.device)

            # linear interpolation path between noise (t=0) and actions (t=1)
            t_expand = t.view(B, 1, 1)
            noisy_actions = (1.0 - (1.0 - sigma_min) * t_expand) * noise + t_expand * actions

            # conditional flow matching target velocity
            velocity_target = actions - (1.0 - sigma_min) * noise

            # predict velocity
            velocity_pred = self.nets["policy"]["velocity_net"](
                noisy_actions, t * self._timestep_embedding_scale(), global_cond=obs_cond)

            # L2 loss
            loss = F.mse_loss(velocity_pred, velocity_target)

            # logging
            losses = {
                "l2_loss": loss
            }
            info["losses"] = TensorUtils.detach(losses)

            if not validate:
                # gradient step
                policy_grad_norms = TorchUtils.backprop_for_loss(
                    net=self.nets,
                    optim=self.optimizers["policy"],
                    loss=loss,
                )

                # update Exponential Moving Average of the model weights
                if self.ema is not None:
                    self.ema.step(self.nets)

                step_info = {
                    "policy_grad_norms": policy_grad_norms
                }
                info.update(step_info)

        return info

    def log_info(self, info):
        """
        Process info dictionary from @train_on_batch to summarize
        information to pass to tensorboard for logging.

        Args:
            info (dict): dictionary of info

        Returns:
            loss_log (dict): name -> summary statistic
        """
        log = super(FlowMatchingUNet, self).log_info(info)
        log["Loss"] = info["losses"]["l2_loss"].item()
        if "policy_grad_norms" in info:
            log["Policy_Grad_Norms"] = info["policy_grad_norms"]
        return log

    def reset(self):
        """
        Reset algo state to prepare for environment rollouts.
        """
        # setup inference queues
        To = self.algo_config.horizon.observation_horizon
        Ta = self.algo_config.horizon.action_horizon
        obs_queue = deque(maxlen=To)
        action_queue = deque(maxlen=Ta)
        self.obs_queue = obs_queue
        self.action_queue = action_queue

    def get_action(self, obs_dict, goal_dict=None):
        """
        Get policy action outputs.

        Args:
            obs_dict (dict): current observation [1, Do]
            goal_dict (dict): (optional) goal

        Returns:
            action (torch.Tensor): action tensor [1, Da]
        """
        if len(self.action_queue) == 0:
            # no actions left, run inference
            # [1,T,Da]
            action_sequence = self._get_action_trajectory(obs_dict=obs_dict, goal_dict=goal_dict)

            # put actions into the queue
            self.action_queue.extend(action_sequence[0])

        # has action, execute from left to right
        # [Da]
        action = self.action_queue.popleft()

        # [1,Da]
        action = action.unsqueeze(0)
        return action

    def _get_action_trajectory(self, obs_dict, goal_dict=None):
        assert not self.nets.training
        To = self.algo_config.horizon.observation_horizon
        Ta = self.algo_config.horizon.action_horizon
        Tp = self.algo_config.horizon.prediction_horizon
        action_dim = self.ac_dim
        num_inference_steps = self.algo_config.fm.num_inference_steps
        solver = self.algo_config.fm.solver

        # select network
        nets = self.nets
        if self.ema is not None:
            nets = self.ema.averaged_model

        # encode obs
        inputs = {
            "obs": obs_dict,
            "goal": goal_dict
        }
        for k in self.obs_shapes:
            # first two dimensions should be [B, T] for inputs
            if inputs["obs"][k].ndim - 1 == len(self.obs_shapes[k]):
                # adding time dimension if not present -- this is required as
                # frame stacking is not invoked when sequence length is 1
                inputs["obs"][k] = inputs["obs"][k].unsqueeze(1)
            assert inputs["obs"][k].ndim - 2 == len(self.obs_shapes[k])
        obs_features = TensorUtils.time_distributed(inputs, nets["policy"]["obs_encoder"], inputs_as_kwargs=True)
        assert obs_features.ndim == 3  # [B, T, D]
        B = obs_features.shape[0]

        # reshape observation to (B,obs_horizon*obs_dim)
        obs_cond = obs_features.flatten(start_dim=1)

        # start from Gaussian noise at t=0 and integrate the ODE to t=1
        naction = torch.randn((B, Tp, action_dim), device=self.device)
        dt = 1.0 / num_inference_steps
        embed_scale = self._timestep_embedding_scale()

        for i in range(num_inference_steps):
            t = i * dt
            t_tensor = torch.full((B,), t * embed_scale, device=self.device)
            velocity = nets["policy"]["velocity_net"](
                sample=naction,
                timestep=t_tensor,
                global_cond=obs_cond
            )
            if solver == "euler":
                naction = naction + dt * velocity
            elif solver == "midpoint":
                mid = naction + 0.5 * dt * velocity
                t_mid = torch.full((B,), (t + 0.5 * dt) * embed_scale, device=self.device)
                velocity_mid = nets["policy"]["velocity_net"](
                    sample=mid,
                    timestep=t_mid,
                    global_cond=obs_cond
                )
                naction = naction + dt * velocity_mid
            else:
                raise ValueError("unsupported flow matching solver: {}".format(solver))

        # process action using Ta
        start = To - 1
        end = start + Ta
        action = naction[:, start:end]
        return action

    def serialize(self):
        """
        Get dictionary of current model parameters.
        """
        return {
            "nets": self.nets.state_dict(),
            "optimizers": { k : self.optimizers[k].state_dict() for k in self.optimizers },
            "lr_schedulers": { k : self.lr_schedulers[k].state_dict() if self.lr_schedulers[k] is not None else None for k in self.lr_schedulers },
            "ema": self.ema.averaged_model.state_dict() if self.ema is not None else None,
        }

    def deserialize(self, model_dict, load_optimizers=False):
        """
        Load model from a checkpoint.

        Args:
            model_dict (dict): a dictionary saved by self.serialize() that contains
                the same keys as @self.network_classes
            load_optimizers (bool): whether to load optimizers and lr_schedulers from the model_dict;
                used when resuming training from a checkpoint
        """
        self.nets.load_state_dict(model_dict["nets"])

        # for backwards compatibility
        if "optimizers" not in model_dict:
            model_dict["optimizers"] = {}
        if "lr_schedulers" not in model_dict:
            model_dict["lr_schedulers"] = {}

        if model_dict.get("ema", None) is not None:
            self.ema.averaged_model.load_state_dict(model_dict["ema"])

        if load_optimizers:
            for k in model_dict["optimizers"]:
                self.optimizers[k].load_state_dict(model_dict["optimizers"][k])
            for k in model_dict["lr_schedulers"]:
                if model_dict["lr_schedulers"][k] is not None:
                    self.lr_schedulers[k].load_state_dict(model_dict["lr_schedulers"][k])
